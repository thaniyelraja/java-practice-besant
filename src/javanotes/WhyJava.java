package javanotes;

public class WhyJava {
    public static void main(String[] args) {

        // ==================================================
        //                 WHY JAVA?
        // ==================================================

        /*
         * 1. Why Java came into the market?
         * ----------------------------------
         * Before Java, languages like C and C++ were widely used.
         * But they had several problems:
         *
         * - Platform dependent (code works only on specific OS)
         * - Complex memory management (manual)
         * - Security issues
         * - Pointer-related errors
         *
         * Java was introduced in 1995 by James Gosling
         * to solve these problems.
         */

        /*
         * 2. Main Goal of Java
         * ---------------------
         * "Write Once, Run Anywhere" (WORA)
         *
         * Java programs can run on any system using JVM
         * without rewriting the code.
         */

        /*
         * 3. Difference between Java and Older Languages
         * -----------------------------------------------
         *
         * C / C++:
         * - Platform dependent
         * - Uses pointers
         * - Manual memory management
         * - Less secure
         *
         * Java:
         * - Platform independent (via JVM)
         * - No pointers (more secure)
         * - Automatic memory management (Garbage Collector)
         * - Strong security model
         */

        /*
         * 4. Is Java Object-Oriented?
         * ----------------------------
         * Java is MOSTLY Object-Oriented but not purely OOP.
         *
         * Why?
         * - Java supports primitive data types (int, char, etc.)
         * - Pure OOP languages treat everything as objects
         *
         * So Java is called "Object-Oriented Programming Language"
         * but not 100% pure OOP.
         */

        /*
         * 5. Why Java is Popular?
         * ------------------------
         *
         * - Platform Independent (WORA)
         * - Easy to learn compared to C++
         * - Secure (no pointers, bytecode verification)
         * - Robust (strong memory management)
         * - Multithreading support
         * - Large community support
         * - Used in:
         *      - Web applications
         *      - Android apps
         *      - Enterprise software
         */

        /*
         * 6. Key Features of Java
         * ------------------------
         *
         * - Simple
         * - Object-Oriented
         * - Platform Independent
         * - Secure
         * - Robust
         * - Portable
         * - Multithreaded
         * - High Performance (JIT)
         */

        /*
         * 7. Why Companies Still Use Java?
         * --------------------------------
         *
         * - Stability and reliability
         * - Huge ecosystem (Spring, Hibernate)
         * - Backward compatibility
         * - Strong community support
         */

        /*
         * 8. Summary (Interview Shortcut)
         * --------------------------------
         * Java was introduced to overcome the limitations of C/C++,
         * mainly platform dependency and memory issues.
         * It uses JVM to achieve platform independence
         * and provides better security and memory management.
         */

        System.out.println("Why Java Notes Loaded Successfully!");
    }
}
