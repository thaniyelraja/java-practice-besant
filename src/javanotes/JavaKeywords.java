package javanotes;

public class JavaKeywords {
    public static void main(String[] args) {

        // ============================================
        //           JAVA KEYWORDS & USAGE
        // ============================================

        /*
         * WHAT ARE KEYWORDS?
         * -------------------
         * Keywords are reserved words in Java.
         * They have predefined meaning and cannot be used as identifiers.
         *
         * Example:
         * int, class, public, static, if, else, etc.
         */

        // ============================================
        // 1. ACCESS MODIFIERS
        // ============================================

        /*
         * public    → accessible everywhere
         * private   → accessible only within class
         * protected → accessible within package + subclass
         */

        // ============================================
        // 2. DATA TYPE KEYWORDS
        // ============================================

        /*
         * int, float, double, char, boolean, byte, short, long
         */

        // ============================================
        // 3. CLASS RELATED KEYWORDS
        // ============================================

        /*
         * class → used to define a class
         * new   → used to create object
         * this  → refers to current object
         * super → refers to parent class
         */

        // ============================================
        // 4. CONTROL FLOW KEYWORDS
        // ============================================

        /*
         * if, else, switch → decision making
         * for, while, do   → looping
         * break, continue  → loop control
         * return           → return value
         */

        // ============================================
        // 5. EXCEPTION HANDLING
        // ============================================

        /*
         * try, catch, finally, throw, throws
         */

        // ============================================
        // 6. OOP KEYWORDS
        // ============================================

        /*
         * extends     → inheritance
         * implements  → interface implementation
         * interface   → define interface
         * abstract    → abstract class/method
         */

        // ============================================
        // 7. NON-ACCESS MODIFIERS
        // ============================================

        /*
         * static → belongs to class
         * final  → constant / cannot be changed
         */

        // ============================================
        // 8. PACKAGE RELATED
        // ============================================

        /*
         * package → defines package
         * import  → imports classes/packages
         */

        // ============================================
        // 9. OTHER IMPORTANT KEYWORDS
        // ============================================

        /*
         * void       → no return type
         * instanceof → checks object type
         * null       → no value
         */

        // ============================================
        // 10. SUMMARY
        // ============================================

        /*
         * Keywords are reserved words in Java.
         * They define structure, logic, and behavior of programs.
         */

        System.out.println("Java Keywords Notes Loaded!");
    }
}