package javanotes;

public class OperatorsVariablesControl {

    // ==========================================================
    //          VARIABLES, OPERATORS & CONTROL STATEMENTS
    // ==========================================================

    // ==========================================================
    // 1. VARIABLES
    // ==========================================================

    /*
     * WHAT IS A VARIABLE?
     * --------------------
     * A variable is a container used to store data values.
     */

    /*
     * TYPES OF VARIABLES
     * -------------------
     * 1. Local Variable
     *    - Declared inside method
     *    - Stored in stack
     *
     * 2. Instance Variable
     *    - Declared inside class (outside method)
     *    - Stored in heap (part of object)
     *
     * 3. Static Variable
     *    - Declared with 'static' keyword
     *    - Stored in Method Area
     *    - Shared among all objects
     */

    /*
     * RULES FOR VARIABLES
     * --------------------
     * - Cannot start with number
     * - Cannot use keywords as variable name
     * - Can contain letters, digits, _, $
     */

    // ==========================================================
    // 2. OPERATORS
    // ==========================================================

    /*
     * WHAT IS AN OPERATOR?
     * ---------------------
     * Operators are symbols used to perform operations on variables/values.
     */

    // ----------------------------------------------------------
    // TYPES OF OPERATORS
    // ----------------------------------------------------------

    /*
     * 1. ARITHMETIC OPERATORS
     * ------------------------
     * +  → addition
     * -  → subtraction
     * *  → multiplication
     * /  → division
     * %  → modulus
     */

    /*
     * 2. RELATIONAL OPERATORS
     * ------------------------
     * == → equal to
     * != → not equal
     * >  → greater than
     * <  → less than
     * >= → greater or equal
     * <= → less or equal
     */

    /*
     * 3. LOGICAL OPERATORS
     * ---------------------
     * && → AND
     * || → OR
     * !  → NOT
     */

    /*
     * 4. ASSIGNMENT OPERATORS
     * ------------------------
     * =  → assign
     * += → add and assign
     * -= → subtract and assign
     * *= → multiply and assign
     * /= → divide and assign
     */

    /*
     * 5. UNARY OPERATORS
     * -------------------
     * ++ → increment
     * -- → decrement
     */

    /*
     * 6. TERNARY OPERATOR
     * --------------------
     * condition ? value1 : value2
     */

    // ==========================================================
    // 3. CONTROL STATEMENTS
    // ==========================================================

    /*
     * WHAT ARE CONTROL STATEMENTS?
     * -----------------------------
     * Used to control the flow of execution.
     */

    // ----------------------------------------------------------
    // TYPES OF CONTROL STATEMENTS
    // ----------------------------------------------------------

    /*
     * 1. DECISION MAKING
     * -------------------
     * if
     * if-else
     * else-if ladder
     * switch
     */

    /*
     * 2. LOOPING STATEMENTS
     * ----------------------
     * for loop
     * while loop
     * do-while loop
     */

    /*
     * 3. JUMP STATEMENTS
     * -------------------
     * break    → exit loop
     * continue → skip iteration
     * return   → exit method
     */

    // ==========================================================
    // FLOW DIAGRAM (SIMPLE)
    // ==========================================================

    /*
     *        Condition
     *          |
     *     -------------
     *     |           |
     *   True        False
     *     |           |
     *   Execute     Skip
     */

    // ==========================================================
    // IMPORTANT INTERVIEW POINTS
    // ==========================================================

    /*
     * - Variables store data
     * - Operators perform operations
     * - Control statements control program flow
     */

    // ==========================================================
    // SUMMARY
    // ==========================================================

    /*
     * Variables, operators, and control statements are
     * the basic building blocks of any Java program.
     */

}