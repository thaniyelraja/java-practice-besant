package javanotes;

public class JavaFeatures {

    // ============================================
    //           FEATURES OF JAVA
    // ============================================

    /*
     * 1. SIMPLE
     * ----------
     * Java is easy to learn and use.
     * It removes complex features of C/C++ like pointers,
     * operator overloading, and manual memory management.
     */

    /*
     * 2. OBJECT-ORIENTED
     * -------------------
     * Java follows OOP concepts:
     * - Encapsulation
     * - Inheritance
     * - Polymorphism
     * - Abstraction
     *
     * Everything is organized in classes and objects.
     */

    /*
     * 3. PLATFORM INDEPENDENT
     * ------------------------
     * Java follows "Write Once, Run Anywhere" (WORA).
     * Code is compiled into bytecode which runs on JVM,
     * so it works on any platform (Windows, Linux, Mac).
     */

    /*
     * 4. SECURE
     * ----------
     * Java provides strong security features:
     * - No pointers
     * - Bytecode verification
     * - Secure class loading
     */

    /*
     * 5. ROBUST
     * ----------
     * Java is strong and reliable.
     * - Automatic memory management (Garbage Collection)
     * - Exception handling
     * - Strong type checking
     */

    /*
     * 6. MULTITHREADED
     * -----------------
     * Java supports multiple threads.
     * It allows execution of multiple tasks simultaneously,
     * improving performance.
     */

    /*
     * 7. PORTABLE
     * ------------
     * Java programs can be easily moved from one system
     * to another without changes.
     */

    /*
     * 8. HIGH PERFORMANCE
     * --------------------
     * Java uses Just-In-Time (JIT) compiler
     * which converts bytecode to machine code at runtime,
     * improving execution speed.
     */

    /*
     * 9. DISTRIBUTED
     * ---------------
     * Java supports distributed computing.
     * It provides libraries for networking (RMI, sockets).
     */

    /*
     * 10. DYNAMIC
     * ------------
     * Java supports dynamic loading of classes at runtime.
     * This makes it flexible and adaptable.
     */

    /*
     * 11. ARCHITECTURE NEUTRAL
     * -------------------------
     * Java bytecode is not dependent on any specific hardware.
     * It can run on any machine with JVM.
     */

    /*
     * 12. INTERPRETED + COMPILED
     * ---------------------------
     * Java is both compiled and interpreted:
     * - Compiled into bytecode
     * - Interpreted by JVM
     */

    /*
     * SUMMARY
     * --------
     * Java is simple, secure, platform-independent,
     * object-oriented, and widely used in real-world applications.
     */

}