package javanotes;

public class JavaHistory {
    public static void main(String[] args) {

        // ==============================
        //        HISTORY OF JAVA
        // ==============================

        /*
         * 1. Origin of Java
         * -----------------
         * Java was developed by James Gosling and his team at Sun Microsystems.
         * The development started in 1991.
         */

        /*
         * 2. Original Name - Oak
         * ----------------------
         * Java was initially named "Oak" after an oak tree outside Gosling's office.
         * Later, it was renamed to "Java" in 1995 due to trademark issues.
         */

        /*
         * 3. Official Release
         * -------------------
         * Java was officially released in 1995 by Sun Microsystems.
         */

        /*
         * 4. Goal of Java
         * ----------------
         * Main goal: "Write Once, Run Anywhere" (WORA)
         * This means Java programs can run on any platform without modification.
         */

        /*
         * 5. Key Features Introduced
         * --------------------------
         * - Platform Independent
         * - Object-Oriented
         * - Simple and Secure
         * - Robust and Portable
         * - Multithreaded
         * - High Performance (with JVM)
         */

        /*
         * 6. Java Virtual Machine (JVM)
         * ----------------------------
         * Java uses JVM to execute bytecode.
         * This enables platform independence.
         */

        /*
         * 7. Evolution of Java
         * --------------------
         * Java has evolved with multiple versions:
         *
         * JDK 1.0 (1996) - Initial release
         * JDK 1.1 (1997) - Inner classes, JDBC
         * J2SE 1.2 (1998) - Swing, Collections
         * J2SE 1.3 (2000) - Performance improvements
         * J2SE 1.4 (2002) - Assertions, NIO
         * J2SE 5.0 (2004) - Generics, Autoboxing, Enhanced for loop
         * Java SE 6 (2006) - Performance updates
         * Java SE 7 (2011) - Try-with-resources, Fork/Join
         * Java SE 8 (2014) - Lambda expressions, Streams (VERY IMPORTANT)
         * Java SE 9 (2017) - Module system
         * Java SE 11 (2018) - LTS version
         * Java SE 17 (2021) - LTS version
         */

        /*
         * 8. Ownership Change
         * -------------------
         * Sun Microsystems was acquired by Oracle Corporation in 2010.
         * Since then, Java is maintained by Oracle.
         */

        /*
         * 9. Types of Java Platforms
         * --------------------------
         * - Java SE (Standard Edition)
         * - Java EE (Enterprise Edition) -> now Jakarta EE
         * - Java ME (Micro Edition)
         */

        /*
         * 10. Why Java is Popular
         * -----------------------
         * - Platform Independent
         * - Huge community support
         * - Used in Web, Mobile (Android), Enterprise apps
         * - Strong memory management
         */

        System.out.println("Java History Loaded Successfully!");
    }
}
