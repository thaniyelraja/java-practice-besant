package javanotes;

public class DataTypes {

    // ============================================
    //            DATA TYPES IN JAVA
    // ============================================

    /*
     * WHAT IS A DATA TYPE?
     * ---------------------
     * A data type specifies the type of data a variable can hold.
     * Example: int, float, char, etc.
     */

    // ============================================
    // TYPES OF DATA TYPES
    // ============================================

    /*
     * 1. Primitive Data Types
     * 2. Non-Primitive (Reference) Data Types
     */

    // ============================================
    // 1. PRIMITIVE DATA TYPES (8 TYPES)
    // ============================================

    /*
     * These are basic built-in data types.
     *
     * Integer Types:
     * byte   → 1 byte  (-128 to 127)
     * short  → 2 bytes
     * int    → 4 bytes
     * long   → 8 bytes (use 'L')
     *
     * Floating Types:
     * float  → 4 bytes (use 'f')
     * double → 8 bytes
     *
     * Character Type:
     * char   → 2 bytes (Unicode)
     *
     * Boolean Type:
     * boolean → true / false
     */

    // ============================================
    // 2. NON-PRIMITIVE DATA TYPES
    // ============================================

    /*
     * These store references (addresses).
     *
     * Examples:
     * - String
     * - Arrays
     * - Classes
     * - Interfaces
     */

    // ============================================
    // DEFAULT VALUES
    // ============================================

    /*
     * byte, short, int, long → 0
     * float, double → 0.0
     * char → '\u0000'
     * boolean → false
     * reference → null
     */

    // ============================================
    // TYPE CASTING
    // ============================================

    /*
     * 1. Implicit Casting (Widening)
     *    smaller → larger
     *
     *    Example:
     *    int → double
     *
     * 2. Explicit Casting (Narrowing)
     *    larger → smaller
     *
     *    Example:
     *    double → int
     */

    // ============================================
    // SIZE SUMMARY
    // ============================================

    /*
     * byte    → 1 byte
     * short   → 2 bytes
     * int     → 4 bytes
     * long    → 8 bytes
     * float   → 4 bytes
     * double  → 8 bytes
     * char    → 2 bytes
     * boolean → JVM dependent
     */

    // ============================================
    // IMPORTANT POINTS
    // ============================================

    /*
     * - Primitive types store actual values
     * - Non-primitive store references
     * - String is not primitive (important)
     * - Default data type for decimal is double
     */

}